def areFollowingPatterns(strings, patterns):
    s = {}
    p = {}

    for i in range(len(patterns)):
        if patterns[i] not in p:
            p[patterns[i]] = strings[i]
        if strings[i] not in s:
            s[strings[i]] = patterns[i]

        if s[strings[i]] != patterns[i] or p[patterns[i]] != strings[i]:
            return False

    return True





